
public class Alphabet {
	
	public static void main(String[] args) {
		
		// 1. Write the shortest possible program that outputs the numbers between 0 and 100 to the console. hint: use a loop.		
		// 2. Write the shortest possible program that outputs all the lower case letters of the alphabet to the console. 
		//hint: use a loop with a 'char' type variable.
		// 3. Write the shortest possible program that outputs all the upper case letters of the alphabet, in reverse order,
		// to the console. 
		
		int counter = 0;
		
		while (counter < 101) 
		{
			
			System.out.println(counter);
			
			counter++;
			
		}

	}

}
